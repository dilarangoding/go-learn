package main

import "fmt"

func main() {

	result := 10 + 10

	fmt.Println(result)

	a := 10
	b := 20

	c := a * b

	fmt.Println(c)

	// Augmented Assignments

	var i = 10
	i += 20

	fmt.Println(i)

	// unary operator

	i++

	fmt.Println(i)

	var negative = -100
	var positive = +100

	fmt.Println(negative)
	fmt.Println(positive)

}
